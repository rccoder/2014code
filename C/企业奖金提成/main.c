#include <stdio.h>
#include <stdlib.h>

int main()
{
   int n, s;
    printf("请输入当月利润（单位：万元）:\n");
    scanf("%d", &n);
    if (n <= 10)
        s = n * 0.1;
    else if (n > 10 && n <= 20)
        s = 10 * 0.1 + (n - 10) * 0.075;
    else if (n > 20 && n <= 40)
        s = 10 * 0.1 + (20 - 10) * 0.075 + (n - 20) * 0.05;
    else if (n > 40 && n <= 60)
        s = 10 * 0.1 + (20 - 10) * 0.075 + (40 - 20) * 0.05 + (n - 40) * 0.03;
    else if (n > 60 && n <= 100)
        s = 10 * 0.1 + (20 - 10) * 0.075 + (40 - 20) * 0.05 + (60 - 40) * 0.03 + (n - 60) * 0.015;
    else if (n > 100)
        s = 10 * 0.1 + (20 - 10) * 0.075 + (40 - 20) * 0.05 + (60 - 40) * 0.03 + (100 - 60) * 0.015 + (n - 100) * 0.01;
    printf ("奖金应该是:\n");
    printf ("%d", s);
    return 0;
}
